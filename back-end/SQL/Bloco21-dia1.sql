-- Utilizando as funções de agregação AVG, MIN, MAX, SUM e COUNT
-- Usando a coluna replacement_cost (valor de substituição), vamos encontrar:
SELECT AVG(replacement_cost) FROM sakila.film; -- 19.984000 (Média entre todos registros)
SELECT MIN(replacement_cost) FROM sakila.film; -- 9.99 (Menor valor encontrado)
SELECT MAX(replacement_cost) FROM sakila.film; -- 29.99 (Maior valor encontrado)
SELECT SUM(replacement_cost) FROM sakila.film; -- 19984.00 (Soma de todos registros)
SELECT COUNT(replacement_cost) FROM sakila.film; -- 1000 registros encontrados (Quantidade)


-- GROUP BY -  ele agrupa todos os registros que possuem o mesmo first_name, evitando duplicações na hora de retornar os dados.
SELECT first_name FROM sakila.actor
GROUP BY first_name;

-- Quando quisermos agrupar, mas também contar:
SELECT first_name, COUNT(*) FROM sakila.actor
GROUP BY first_name;

